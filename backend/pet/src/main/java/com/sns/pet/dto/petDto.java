package com.sns.pet.dto;

public class petDto {
    private String userNumber;
    private int petNumber;
    private int animalNumber;
    private String petName;
    private String petGender;
    private String petBirth;
    private String petProfilePicture;
    private String petProfileContent;

    public String getUserNumber() {
        return userNumber;
    }

    public void setUserNumber(String userNumber) {
        this.userNumber = userNumber;
    }

    public int getPetNumber() {
        return petNumber;
    }

    public void setPetNumber(int petNumber) {
        this.petNumber = petNumber;
    }

    public int getAnimalNumber() {
        return animalNumber;
    }

    public void setAnimalNumber(int animalNumber) {
        this.animalNumber = animalNumber;
    }

    public String getPetName() {
        return petName;
    }

    public void setPetName(String petName) {
        this.petName = petName;
    }

    public String getPetGender() {
        return petGender;
    }

    public void setPetGender(String petGender) {
        this.petGender = petGender;
    }

    public String getPetBirth() {
        return petBirth;
    }

    public void setPetBirth(String petBirth) {
        this.petBirth = petBirth;
    }

    public String getPetProfilePicture() {
        return petProfilePicture;
    }

    public void setPetProfilePicture(String petProfilePicture) {
        this.petProfilePicture = petProfilePicture;
    }

    public String getPetProfileContent() {
        return petProfileContent;
    }

    public void setPetProfileContent(String petProfileContent) {
        this.petProfileContent = petProfileContent;
    }
}
